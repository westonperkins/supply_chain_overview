def snapshot_severity(graph: SupplyChainGraph, captured_at_pass: str) -> dict:
    """Capture the state a future pass will diff against. Returns a dict
    with a `captured_at_pass` label (H4 fix) and a `nodes` map keyed by
    node id. The label names the pass whose END state this snapshot
    represents — so a future diff can say plainly which pass it is
    comparing against, and a first-run diff can say `first-run, no
    prior snapshot` unambiguously."""
    return {
        "captured_at_pass": captured_at_pass,
        "nodes": {
            n.id: {
                "severity": n.dynamic.current_severity,
                "tier": n.dynamic.chokepoint_tier.value if n.dynamic.chokepoint_tier else "none",
                "concentration": n.dynamic.concentration,
                "inbound_hhi": n.dynamic.inbound_hhi,
                "outbound_criticality": n.dynamic.outbound_criticality,
            }
            for n in graph.nodes.values()
        },
    }


def build_severity_diff(snapshot: dict, graph: SupplyChainGraph) -> str:
    """Return the docs/generated/severity_diff.md contents.

    Emits every node with severity_before / severity_after / delta so
    a report claim of "nothing moved" has a file behind it. The diff
    header states plainly whether it is a first-run (empty snapshot)
    or a genuine comparison against a named prior pass (H4 fix).

    Also lists any node in the graph that is missing from the snapshot
    (or vice versa) — those are the kinds of drift that would silently
    make a diff look clean when it wasn't."""
    label = snapshot.get("captured_at_pass") if snapshot else None
    nodes_map = snapshot.get("nodes", {}) if snapshot else {}

    lines: list[str] = []
    lines.append("# Severity diff — generated")
    lines.append("")
    lines.append("Generated by `backend/app/reporting/inventory.py::build_severity_diff`.")
    if not nodes_map:
        lines.append("")
        lines.append("_**First-run comparison:** no prior snapshot is present, so every "
                     "delta below is against an empty reference. Not a valid before/after "
                     "check of a scoring change until the next pass._")
    else:
        lines.append(f"Comparison of prior-pass state `{label or 'unlabelled'}` "
                     f"against the current graph.")
    lines.append("")

    snap_ids = set(nodes_map.keys())
    cur_ids = {n.id for n in graph.nodes.values()}
    only_snap = snap_ids - cur_ids
    only_cur = cur_ids - snap_ids
    if only_snap or only_cur:
        lines.append("## Membership drift")
        lines.append("")
        if only_snap:
            lines.append(f"Nodes in snapshot but not in graph: {sorted(only_snap)}")
        if only_cur:
            lines.append(f"Nodes in graph but not in snapshot: {sorted(only_cur)}")
        lines.append("")

    lines.append("## Per-node severity comparison")
    lines.append("")
    lines.append("| id | severity_before | severity_after | delta | tier_before | tier_after |")
    lines.append("|---|---|---|---|---|---|")

    non_zero_deltas = 0
    tier_changes = 0
    common = sorted(snap_ids & cur_ids)
    for nid in common:
        before = nodes_map[nid]
        node = graph.nodes[nid]
        sev_b = before.get("severity")
        sev_a = node.dynamic.current_severity
        tier_b = before.get("tier", "—")
        tier_a = node.dynamic.chokepoint_tier.value if node.dynamic.chokepoint_tier else "none"

        if sev_b is None and sev_a is None:
            delta_str = "0"
        elif sev_b is None or sev_a is None:
            delta_str = "NULL_TRANSITION"
            non_zero_deltas += 1
        else:
            d = sev_a - sev_b
            delta_str = f"{d:+.10f}"
            if abs(d) > 1e-12:
                non_zero_deltas += 1

        if tier_b != tier_a:
            tier_changes += 1

        lines.append(
            f"| {nid} | {_fmt_float(sev_b, 10)} | {_fmt_float(sev_a, 10)} | "
            f"{delta_str} | {tier_b} | {tier_a} |"
        )

    lines.append("")
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- Non-zero severity deltas: **{non_zero_deltas}**")
    lines.append(f"- Tier changes: **{tier_changes}**")
    lines.append("")
    if non_zero_deltas == 0 and tier_changes == 0:
        lines.append("_No severity or tier movement — this pass did not touch scoring._")
    elif non_zero_deltas == 0 and tier_changes > 0:
        lines.append(
            "_Zero severity deltas, tiers rebucketed. Expected shape when the "
            "pass touches the tier boundary set but not the severity formula. "
            "The pass report must explain the boundary change and list every "
            "tier reassignment._"
        )
    else:
        lines.append("_Severity moved. The pass author must explain each delta in the pass report._")
    lines.append("")

    return "\n".join(lines) + "\n"
